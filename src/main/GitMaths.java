package main;

public class GitMaths {

	
	public GitMaths() {

	}
	
	public static void main(String[] args) {
		System.out.println("1+2 = " + add(1,2));
		System.out.println("3+2 = " + multiply(3,2));
		
	}
	
	
	public static int add(int a, int b) {
		return a+b;
	}

	public static int multiply(int a, int b) {
		return a*b;
	}
	
	
	
	
}
